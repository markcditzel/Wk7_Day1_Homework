public class Stall {

    // PROPERTIES

    private int rating;
    private String name;
    private String ownerName;
    private int parkingSpot;

    // CONSTRUCTOR

    public Stall(int rating, String name, String ownerName, int parkingSpot){
        this.rating = rating;
        this.name = name;
        this.ownerName = ownerName;
        this.parkingSpot = parkingSpot;
    }
}
